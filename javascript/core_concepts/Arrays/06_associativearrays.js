// Many programming languages support arrays named indexes
// Arrays with named inexes are called associative arrays (or hashes)

const person = [];
person["firstName"] = "John";
person["lastName"] = "Doe";
person["age"] = 46;
person.length;     // Will return 0
person[0];         // Will return undefined

// Differences between arrays and objects
// In Js, arrays are numbered indexes
// In js, Objects are named indexes