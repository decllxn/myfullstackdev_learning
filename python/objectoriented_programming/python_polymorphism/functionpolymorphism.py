# polymorphism means many forms
# in programming it refers to methods, functions operators with the same nae that can be executed on many objects or classess

# example of a py function len()

# len returning length of characters in a string
x = "Hello World!"
print(len(x))

# len returning length of items in a tuple
thistuple = ("apple" , "banana", "Cherry")
print(len(thistuple))

# len returning the key/value pairs in the dictionary
mycar = {
    "brand": "Ford",
    "model": "Mustang",
    "year": "1964"
}

print(len(mycar))

# len has many forms, this is called polymorphism
